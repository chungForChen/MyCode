package com.ssmsim.model;

public class Report {
    private int boysNumber;
    private int girlsNumber;
    private int AGradeNumber;
    private int BGradeNumber;
    private int CGradeNumber;
    private int DGradeNumber;
    private int EGradeNumber;

    public Report() {
    }

    public int getBoysNumber() {
        return boysNumber;
    }

    public void setBoysNumber(int boysNumber) {
        this.boysNumber = boysNumber;
    }

    public int getGirlsNumber() {
        return girlsNumber;
    }

    public void setGirlsNumber(int girlsNumber) {
        this.girlsNumber = girlsNumber;
    }

    public int getAGradeNumber() {
        return AGradeNumber;
    }

    public void setAGradeNumber(int AGradeNumber) {
        this.AGradeNumber = AGradeNumber;
    }

    public int getBGradeNumber() {
        return BGradeNumber;
    }

    public void setBGradeNumber(int BGradeNumber) {
        this.BGradeNumber = BGradeNumber;
    }

    public int getCGradeNumber() {
        return CGradeNumber;
    }

    public void setCGradeNumber(int CGradeNumber) {
        this.CGradeNumber = CGradeNumber;
    }

    public int getDGradeNumber() {
        return DGradeNumber;
    }

    public void setDGradeNumber(int DGradeNumber) {
        this.DGradeNumber = DGradeNumber;
    }

    public int getEGradeNumber() {
        return EGradeNumber;
    }

    public void setEGradeNumber(int EGradeNumber) {
        this.EGradeNumber = EGradeNumber;
    }
}
